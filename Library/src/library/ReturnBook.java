    public void ref(){
        
        try{
            
            DefaultTableModel mod = (DefaultTableModel)jTable1.getModel();
            mod.setRowCount(0);
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/librarydb?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "1998");
            
            Statement stm = con.createStatement();
            
            String sql = "SELECT * FROM issueandreturnbooks";
            
            ResultSet rs = stm.executeQuery(sql);
            
            while(rs.next()){
                
                String d1 = rs.getString("UserID");
                String d2 = rs.getString("BookID");
                String d3 = rs.getString("IssueDate");
                String d4 = rs.getString("ReturnDate");
                String d5 = rs.getString("ProvideStaffID");
                
                mod.addRow(new Object[] {d1, d2, d3, d4, d5});
            }
            
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Error while table load..."+"\n"+e);
        }
    }

