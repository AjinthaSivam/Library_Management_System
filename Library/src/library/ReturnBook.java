    private void btnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchActionPerformed
        // TODO add your handling code here:
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/librarydb?zeroDateTimeBehavior=CONVERT_TO_NULL", "root", "1998");
            
            String mid = txtmid.getText();
            String bid = txtbid.getText();
            
           
            
            Statement stm = con.createStatement();
            
            String sql = "SELECT * FROM issueandreturnbooks WHERE UserID = '"+mid+"' and BookID = '"+bid+"'";
            
            ResultSet rs = stm.executeQuery(sql);
            
            if(rs.next()){
                
                
                lblidate.setText(rs.getString(3));
                lblsid.setText(rs.getString(5));
                
            }else{
                
                JOptionPane.showMessageDialog(this, "UserID and BookID are not matched!");
            }
            
            
            con.close();
            
            
            
        }catch (Exception e){
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_btnsearchActionPerformed

