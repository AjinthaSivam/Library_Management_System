    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        try{

            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/librarydb?", "root", "1998");

            int row = jTable1.getSelectedRow();
            String id = (jTable1.getModel().getValueAt(row, 0).toString());
            String sql = "SELECT * FROM issueandreturnbooks WHERE UserID = '"+id+"'";
            Statement stm = con.createStatement();

            ResultSet rs = stm.executeQuery(sql);

            if(rs.next()){

                txtmid.setText(rs.getString("UserID"));
                txtbid.setText(rs.getString("BookID"));
                txtidate.setText(rs.getString("IssueDate"));
                txtrdate.setText(rs.getString("ReturnDate"));
                txtsid.setText(rs.getString("ProvideStaffID"));

            }

            btnclear.setEnabled(true);

        }catch(Exception e){

            JOptionPane.showMessageDialog(this, "Error while mouse click on Table"+"\n"+e);
        }
    }//GEN-LAST:event_jTable1MouseClicked

